<tr>
    <td>Ask (Offer)</td>
    <td>
        Price of the offer, the price you buy for.
    </td>
</tr>
<tr>
    <td>Bank Rate</td>
    <td>
        The percentage rate at which central bank of a country lends money to the country's commercial banks.
    </td>
</tr>
<tr>
    <td>Bid</td>
    <td>
        Price of the demand, the price you sell for.
    </td>
</tr>
<tr>
    <td>Broker</td>
    <td>
        The market participating body which serves as the middleman between retail traders and larger commercial
        institutions.
    </td>
</tr>
<tr>
    <td>Carry Trade</td>
    <td>
        In Forex, holding a position with a positive overnight interest return in hope of gaining profits, without
        closing the position, just for the central banks interest rates difference.
    </td>
</tr>
<tr>
    <td>CFD</td>
    <td>
        A Contract for Difference — special trading instrument that allows financial speculation on stocks, commodities
        and other instruments without actually buying.
    </td>
</tr>
<tr>
    <td>Commission</td>
    <td>
        Broker commissions for operation handling.
    </td>
</tr>
<tr>
    <td>CPI</td>
    <td>
        Consumer price index the statistical measure of inflation based upon changes of prices of a specified set of
        goods.
    </td>
</tr>
<tr>
    <td>EA (Expert Advisor)</td>
    <td>
        An automated script which is used by the trading platform software to manage positions and orders automatically
        without (or with little) manual control.
    </td>
</tr>
<tr>
    <td>ECB (European Central Bank)</td>
    <td>
        The main regulatory body of the European Union financial system.
    </td>
</tr>
<tr>
    <td>Fed (Federal Reserve)</td>
    <td>
        The main regulatory body of the United States of America financial system, which division — FOMC (Federal Open
        Market Committee) — regulates, among other things, federal interest rates.
    </td>
</tr>
<tr>
    <td>Flat (Square)</td>
    <td>
        Neutral state when all your positions are closed.
    </td>
</tr>
<tr>
    <td>Floating Leverage</td>
    <td>
        A leverage that changes depending on the total size of open positions.
    </td>
</tr>
<tr>
    <td>Fundamental Analysis</td>
    <td>
        The analysis based only on news, economic indicators and global events.
    </td>
</tr>
<tr>
    <td>Gap</td>
    <td>
        A difference between the previous period's close price and the next period's open price. In Forex usually only
        occurs during weekends — between the Friday's close and the Monday's open price.
    </td>
</tr>
<tr>
    <td>GDP (Gross Domestic Product)</td>
    <td>
        Is a measure of the national income and output for the country's economy; it's one of the most important Forex
        indicators.
    </td>
</tr>
<tr>
    <td>GTC (Good 'Til Canceled)</td>
    <td>
        Order to buy or sell of a currency with a fixed price or worse. The order is alive (good) until execution or
        cancellation.
    </td>
</tr>
<tr>
    <td>Hedging</td>
    <td>
        Maintaining a market position which secures the existing open positions in the opposite direction.
    </td>
</tr>
<tr>
    <td>Leading Indicators</td>
    <td>
        A composite index (year 1992 = 100%) of ten most important macroeconomic indicators that predicts future (6-9
        months) economic activity.
    </td>
</tr>
<tr>
    <td>Limit Order</td>
    <td>
        Order for a broker to buy the lot for fixed or lesser price or sell the lot for fixed or better price. Such
        price is called limit price.
    </td>
</tr>
<tr>
    <td>Liquidity</td>
    <td>
        The measure of markets which describes relationship between the trading volume and the price change.
    </td>
</tr>
<tr>
    <td>Long</td>
    <td>
        The position which is in a Buy direction. In Forex, the primary currency when bought is long and another is
        short.
    </td>
</tr>
<tr>
    <td>Loss</td>
    <td>
        The loss from closing long position at lower rate than opening or short position with higher rate than opening,
        or if the profit from a position closing was lower than broker commission on it.
    </td>
</tr>
<tr>
    <td>Lot</td>
    <td>
        Definite amount of units or amount of money accepted for operations handling (usually it is a multiple of 100).
    </td>
</tr>
<tr>
    <td>Margin</td>
    <td>
        Money, the investor needs to keep at broker account to execute trades. It supplies the possible losses which may
        occur in margin trading.
    </td>
</tr>
<tr>
    <td>Margin Account</td>
    <td>
        Account which is used to hold investor's deposited money for FOREX trading.
    </td>
</tr>
<tr>
    <td>Margin Call</td>
    <td>
        Demand of a broker to deposit more margin money to the margin account when the amount in it falls below certain
        minimum.
    </td>
</tr>
<tr>
    <td>Market Order</td>
    <td>
        Order to buy or sell a lot for a current market price.
    </td>
</tr>
<tr>
    <td>Market Price</td>
    <td>
        The current price for which the currency is traded for on the market.
    </td>
</tr>
<tr>
    <td>Momentum</td>
    <td>
        The measure of the currency's ability to move in the given direction.
    </td>
</tr>
<tr>
    <td>Offer (Ask)</td>
    <td>
        Price of the offer, the price you buy for.
    </td>
</tr>
<tr>
    <td>Open Position (Trade)</td>
    <td>
        Position on buying (long) or selling (short) for a currency pair.
    </td>
</tr>
<tr>
    <td>Order</td>
    <td>
        Order for a broker to buy or sell the currency with a certain rate.
    </td>
</tr>
<tr>
    <td>Percentage Allocation Management Module (PAMM)</td>
    <td>
        A broker-side system that allows investor to invest with traders, and allows traders to manage investors' funds
        using the broker's platform.
    </td>
</tr>
<tr>
    <td>Pivot Point</td>
    <td>
        The primary support/resistance point calculated basing on the previous trend's High, Low and Close prices.
    </td>
</tr>
<tr>
    <td>Pip (Point)</td>
    <td>
        The last digit in the rate (e.g. for EUR/USD 1 point = 0.0001).
    </td>
</tr>
<tr>
    <td>Profit (Gain)</td>
    <td>
        Positive amount of money gained for closing the position.
    </td>
</tr>
<tr>
    <td>Principal Value</td>
    <td>
        The initial amount of money of the invested.
    </td>
</tr>
<tr>
    <td>Realized Profit/Loss</td>
    <td>
        Gain/loss for already closed positions.
    </td>
</tr>
<tr>
    <td>Resistance</td>
    <td>
        Price level for which the intensive selling can lead to price increasing (up-trend).
    </td>
</tr>
<tr>
    <td>Scalping</td>
    <td>
        A style of trading notable by many positions that are opened for extremely small and short-term profits.
    </td>
</tr>
<tr>
    <td>Settled (Closed) Position</td>
    <td>
        Closed positions for which all needed transactions has been made.
    </td>
</tr>
<tr>
    <td>Slippage</td>
    <td>
        Execution of order for a price different than expected (ordered), main reasons for slippage are — "fast" market,
        low liquidity and low broker's ability to execute orders.
    </td>
</tr>
<tr>
    <td>Spread</td>
    <td>
        Difference between ask and bid prices for a currency pair.
    </td>
</tr>
<tr>
    <td>Standard Lot</td>
    <td>
        100,000 units of the base currency of the currency pair, which you are buying or selling.
    </td>
</tr>
<tr>
    <td>Stop-Limit Order</td>
    <td>
        An order to sell or buy a lot for a certain price or worse.
    </td>
</tr>
<tr>
    <td>Stop-Loss Order (SL)</td>
    <td>
        An order to sell or buy a lot when the market reaches certain price. It is used to avoid extra losses when
        market moves in the opposite direction. Usually is a combination of stop-order and limit-order.
    </td>
</tr>
<tr>
    <td>STP (Straight Through Processing)</td>
    <td>
        An order processing that doesn't require any manual intervention and is fully automatic. In fact, 99.9% of all
        on-line Forex brokers support order handling with STP.
    </td>
</tr>
<tr>
    <td>Support</td>
    <td>
        Price level for which intensive buying can lead to the price decreasing (down-trend).
    </td>
</tr>
<tr>
    <td>Swap</td>
    <td>
        Overnight payment for holding your position. Since you are not physically receiving the currency you buy, your
        broker should pay you the interest rate difference between the two currencies of the pair. It can be negative or
        positive.
    </td>
</tr>
<tr>
    <td>Take-Profit Order (TP)</td>
    <td>
        An order to sell or buy a lot when the market reaches certain price. It is used to fixate your profit. Usually
        is a combination of stop-order and limit-order.
    </td>
</tr>
<tr>
    <td>Technical Analysis</td>
    <td>
        The analysis based only on the technical market data (quotes) with the help of various technical indicators.
    </td>
</tr>
<tr>
    <td>Trend</td>
    <td>
        Direction of market which has been established with influence of different factors.
    </td>
</tr>
<tr>
    <td>Unrealized (Floating) Profit/Loss</td>
    <td>
        A profit/loss for your non-closed positions.
    </td>
</tr>
<tr>
    <td>Useable Margin</td>
    <td>
        Amount of money in the account that can be used for trading.
    </td>
</tr>
<tr>
    <td>Used Margin</td>
    <td>
        Amount of money in the account already used to hold open positions open.
    </td>
</tr>
<tr>
    <td>Volatility</td>
    <td>
        A statistical measure of the number of price changes for a given currency pair in a given period of time.
    </td>
</tr>
<tr>
    <td>VPS (Virtual Private Server)</td>
    <td>
        Virtual environment hosted on the dedicated server, which can be used to run the programs independent on the
        user's PC. Forex traders use VPS to host trading platforms and run expert advisors without unexpected
        interruptions.
    </td>
</tr>
